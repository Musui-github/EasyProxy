class SetScorePacket
{
    player;

    /** @param Player {Player} */
    constructor(Player)
    {
        this.player=Player;
    }

    /**
     * @param score {number}
     * @param value {string}
     */
    create(score, value)
    {
        this.player.getBedrockPlayer().queue("set_score", {
            action: 'change',
            entries: [{
                scoreboard_id: score,
                objective_name: 'objective',
                score: score,
                entry_type: 'fake_player',
                custom_name: value
            }]
        });
    }
}

module.exports = SetScorePacket;