class MobEffectPacket
{
    player;

    /** @param Player {Player} */
    constructor(Player)
    {
        this.player=Player;
    }

    /**
     * @param action
     * @param id
     * @param duration
     * @param amplifier
     * @param particles
     */
    create(action, id, duration, amplifier, particles)
    {
        this.player.getBedrockPlayer().queue("mob_effect", {
            runtime_entity_id: this.player.getUniqueID(),
            event_id: action,
            effect_id: id,
            duration: duration,
            amplifier: amplifier,
            particles: particles,
        });
    }
}

module.exports = MobEffectPacket;