class SetDisplayObjectivePacket
{
    player;

    /** @param Player {Player} */
    constructor(Player)
    {
        this.player=Player;
    }

    /**
     * @param name {string}
     */
    create(name)
    {
        this.player.getBedrockPlayer().queue("set_display_objective", {
            display_slot: 'sidebar',
            objective_name: 'objective',
            display_name: name,
            criteria_name: 'dummy',
            sort_order: 0
        });
    }
}

module.exports = SetDisplayObjectivePacket;