const ServerInfo = require("./ServerInfo");
const {Server} = require("./Server");
const EasyProxyInfo = require("./EasyProxyInfo");

class EasyProxy
{
    server;

    constructor(data, messages = true)
    {
        this.server=new Server(data, messages);
        ServerInfo.setServer(this.server);
        EasyProxyInfo.setInitialEasyInstance(this);
    }

    others(serverData, address, port)
    {
        this.server=new Server({
            address: serverData[0],
            port: serverData[1],

            destHost: address,
            destPort: port
        }, false);
        ServerInfo.setServer(this.server);
    }
}

module.exports = EasyProxy;