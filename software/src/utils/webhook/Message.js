class Message
{
    data = {};

    constructor() {}

    /**
     * @param name {string}
     */
    setUsername(name)
    {
        this.data.username=name;
    }

    /**
     * @param url {string}
     */
    setAvatarUrl(url)
    {
        this.data.avatar_url=url;
    }

    /**
     * @param str {string}
     */
    setContent(str)
    {
        this.data.content=str;
    }

    addEmbed(embed)
    {
        this.data.embeds.push(embed.getData());
    }

    getData()
    {
        return this.data;
    }
}
module.exports = Message;